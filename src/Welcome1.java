    private void CancCustButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancCustButtonActionPerformed
        
        String cancel_Customer = CancelCustomerCombo.getItemAt (CancelCustomerCombo.getSelectedIndex());
        //String cancel_Date = CancDateCombo.getItemAt (CancDateCombo.getSelectedIndex());
        Date cancel_Date = Date.valueOf(CancDateCombo.getSelectedItem().toString());
        int number = 0;
        String status = "";
        String currentFlight;
        currentFlight = Booking.getBookingByCustDate(cancel_Customer, cancel_Date);
        if (currentFlight != null)
        {
            System.out.println("Customer was found in bookings, removing now");
            System.out.println("Current flight is " + currentFlight);
            Booking.cancelBooking(cancel_Customer, cancel_Date);
            status += "Customer " + cancel_Customer + " Cancelled.";
            
            String book_customer = Waitlist.waitlistByFlightDate(currentFlight, cancel_Date);
            if (book_customer != null)
            {
                java.sql.Timestamp newTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
                Booking.bookByFlightDate(cancel_Date,currentFlight, book_customer, newTimestamp);
                status +=  book_customer + " was added";
                Waitlist.cancelWaitlist(book_customer, cancel_Date);
            }
            
            
            CancelBookingTextArea.setText(status);
            
        }
        else
        {
            System.out.println("Customer was not found in bookings, Checking Waitlist");
            number = Waitlist.cancelWaitlist(cancel_Customer, cancel_Date);
            
            if (number == 0)
            {
                CancelBookingTextArea.setText("Selected customer is not booked/waitlisted for this date");
            }
            else
            {
                CancelBookingTextArea.setText("Selected customer removed from waitlist for this date");
            }
        }
    }//GEN-LAST:event_CancCustButtonActionPerformed

