    private void AddBookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBookingButtonActionPerformed
        String flightType = (String) BookingFlightCombo.getSelectedItem();
        String customerName =  (String) CustomerNameCombo.getSelectedItem();
        Date flightDate=null;
        SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd");
        try {
            java.util.Date date = sdf1.parse((String) BookingDateCombo.getSelectedItem());
            flightDate = new java.sql.Date(date.getTime());
        } catch (ParseException ex) {
            Logger.getLogger(Welcome1.class.getName()).log(Level.SEVERE, null, ex);
        }
         
        
        int total_seats = Flight.getSeats(flightType);
        int booked_seats = Booking.getBookCount(flightType, flightDate);
        
        java.sql.Timestamp customerPosition = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        System.out.println(total_seats);
        System.out.println(booked_seats);
        if (total_seats > booked_seats)
        {
            Booking.addBooking(customerName, flightDate, flightType);
            BookStatus.setText("Customer was booked.");
            
        }
        else
        {
            Waitlist.addWaitlist(customerName, flightDate, flightType, customerPosition);
            BookStatus.setText("Customer was waitlisted");       
        }
    }//GEN-LAST:event_AddBookingButtonActionPerformed

